package beans;

public class New_User {
    
    private int uid;
    private String name;
    private int marks;

    public int getUid() {
        return uid;
    }

    public String getName() {
        return name;
    }

    public int getMarks() {
        return marks;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }
    
    
}
