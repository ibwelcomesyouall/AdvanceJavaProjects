
package beans;

public class Course {
    
    private int cid;
    private String cname;
    private int fee;

    public int getCid() {
        return cid;
    }

    public String getCname() {
        return cname;
    }

    public int getFee() {
        return fee;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public void setFee(int fee) {
        this.fee = fee;
    }
    
    
    
}
